# Integer Type
num: int = 10
print(type(num))
print(num)

# Float Type
decimalNum: float = 10.5
print(type(decimalNum))
print(decimalNum)

# String Type
hello: str = "Hello, Python!"
print(type(hello))
print(hello)

# Boolean Type
boolType: bool = True
print(type(boolType))
print(boolType)

# List Type
fruitsList: list = ["apple", "banana", "cherry"]
print(type(fruitsList))
print(fruitsList)

# Dictionary Type
personObj: dict = {"name": "Alice", "age": 25, "city": "New York"}
print(type(personObj))
print(personObj)


# Set Type
unique_numbers: set = {1, 2, 3, 4, 5}
print(type(unique_numbers))
print(unique_numbers)

# Tuple Type
typleType: tuple = (10, 20)
print(type(typleType))
print(typleType)

# Bytes Type
byte_data: bytes = b"hello"
print(type(byte_data))
print(byte_data)

# FrozenSet Type
frozen_numbers: frozenset = frozenset({1, 2, 3, 4, 5})
print(type(frozen_numbers))
print(frozen_numbers)

# None Type
nothing: None = None
print(type(nothing))
print(nothing)
